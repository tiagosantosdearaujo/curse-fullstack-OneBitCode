let velocity = prompt("Entry with your velocity\n50 km/h\n80 km/h\n100 km\h")

switch(velocity * 2){
    case 100:
        alert("Your velocity is 100 km/h")
        break
    case 160:
        alert("Your velocity is 160 km/h")
        break
    case 200:
        alert("Your velocity is 200 km/h")
        break
    default:
        alert("Error")
}